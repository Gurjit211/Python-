#Example: iterating over a string:

name = 'Gurjit'
for i in name:
    print(i, end=", ")

print("\n")

#Example: iterating over a tuple:

colors = ("Red", "Green", "Blue", "Yellow")
for x in colors:
    print(x)
print("\n")


#Here, we can use the range() function.

for k in range(5):
    print(k)

print("\n")


#But we can also loop over a specific range.

for k in range(4, 9):
    print(k)

print("\n")


#print even numbers using  range

for e in range(2,10,2):
    print(e)

print("\n")




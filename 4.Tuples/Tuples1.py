'''
Just like lists, Python also provides another way to store multiple items: tuples. Tuples look similar to lists,
but have one important difference: they cannot be changed after creation.
'''
tuple1 = (1, 2, 2, 3, 5, 4, 6)
tuple2 = ("Red", "Green", "Blue")
print(tuple1)
print(tuple2)

details = ("Abhijeet", 18, "FYBScIT", 9.8)
print(details)

# Comparison operators return True or False based on the comparison:

# Comparison Operators Demo

# Initialize new variables
alpha = 5
beta = 3

# Equal to
print("Is alpha equal to beta?", alpha == beta)

# Not equal to
print("Is alpha not equal to beta?", alpha != beta)

# Greater than
print("Is alpha greater than beta?", alpha > beta)

# Less than
print("Is alpha less than beta?", alpha < beta)

# Greater than or equal to
print("Is alpha greater than or equal to beta?", alpha >= beta)

# Less than or equal to
print("Is alpha less than or equal to beta?", alpha <= beta)
